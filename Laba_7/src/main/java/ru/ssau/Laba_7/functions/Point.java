package ru.ssau.Laba_7.functions;

public class Point {
    private Double x = 0.0;
    private Double y = 0.0;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
